package streams;

public class PersonDept {

	private int id;
	private String name;
	public int getId() {
		return id;
	}
	public PersonDept setId(int id) {
		this.id = id;
		return this;
	}
	public String getName() {
		return name;
	}
	public PersonDept setName(String name) {
		this.name = name;
		return this;
	}
	
}
